export const addresEmpty: AddressType = {
    country: '',
    state: '',
    city: '',
    neighborhood: '',
    street: '',
    department: ''
};

/**
 * use Address
 * Custom hook
 * @returns 
 */
export default function useAddress() {

    const getCurrentCountry = (): string => {
        return GlobalConfig.defaultCountry;
    };

    const getInitialAddress = (country: string): AddressType => {
        const addrs = {
            ...addresEmpty,
            country: country
        }
        return addrs;
    };

    const getStates = (country: string): string[] => {
        const reg: string[] = RegionsAndCommunesCL.map(function (element) {
            return element.region;
        });
        return reg;
    };

    const getNeighborhood = (country: string, regionSelected: string): string[] => {
        const reg = RegionsAndCommunesCL.map(function (element) {
            return element.region;
        });

        const el = RegionsAndCommunesCL.find(element => element.region === regionSelected);
        const communes: string[] = el ? el.communes : [];
        return communes;
    };

    const convertAddressToString = (address: AddressType) => {
        if (!address) return '';
        return address?.street + " " + address?.department
            + ", " + address?.neighborhood + ", " + address?.state
            + ", " + address?.city + ", " + address?.country;
    }

    return {
        getInitialAddress,
        getStates,
        getNeighborhood,
        getCurrentCountry,
        convertAddressToString
    };
};